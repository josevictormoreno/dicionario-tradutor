def lingua_selecionada(lingua):
    if lingua == 'Espanhol':
        return 'es'
    if lingua == 'Italiano':
        return 'it'
    if lingua == 'Frances':
        return 'fr'
    if lingua == 'Ingles':
        return 'en'
    if lingua == 'Portugues':
        return 'pt'
    if lingua == 'Japones':
        return 'ja'
    if lingua == 'Indiano':
        return 'hi'
    if lingua == 'Russo':
        return 'ru'
    if lingua == 'Arabe':
        return 'ar'
